class UNet(nn.Module):
    def __init__(self, n_channels, n_classes, bilinear=True, channel=64):
        super(UNet, self).__init__()
        self.n_channels = n_channels
        self.n_classes = n_classes
        self.bilinear = bilinear

        self.inc = DoubleConv(n_channels, channel)
        self.down1 = Down(channel, 2 * channel)
        self.down2 = Down(2 * channel, 4 * channel)
        self.down3 = Down(4 * channel, 8 * channel)
        factor = 2 if bilinear else 1
        self.down4 = Down(8 * channel, 16 * channel // factor)

        # self.ca = SEAttention(1024 // factor)
        # self.ca = SKAttention(1024 // factor)
        # self.ca = CBAMBlock(1024 // factor)
        # self.ca = TripletAttention()
        # self.ca = DAModule1(d_model=512,kernel_size=3,H=7,W=7)

        # self.ca = Patch_ECA_OnePath(1024 // factor)

        self.up1 = Up(16 * channel, 8 * channel // factor, bilinear)
        self.up2 = Up(8 * channel, 4 * channel // factor, bilinear)
        self.up3 = Up(4 * channel, 2 * channel // factor, bilinear)
        self.up4 = Up(2 * channel, channel, bilinear)
        self.outc = OutConv(channel, n_classes)

    def forward(self, x):
        x1 = self.inc(x)
        x2 = self.down1(x1)
        x3 = self.down2(x2)
        x4 = self.down3(x3)
        x5 = self.down4(x4)

        # x5 = self.ca(x5)

        x = self.up1(x5, x4)
        x = self.up2(x, x3)
        x = self.up3(x, x2)
        x = self.up4(x, x1)
        logits = self.outc(x)
        return logits


